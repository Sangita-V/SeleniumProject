package Day5;

public class WhileLoopDemo {
	
	public static void main(String[] args) {
		
		//print 1-10 numbers
		
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
		//print even numbers
		int i1=2;
		while(i1<=10)
		{
		    System.out.println(i1);
		    i1+=2;  // i = i+2
		}
		

	}

}
